def main() -> None:
    """
    Command-line entry point.

    This function allows the Semantic Layer to be executed with:

        python -m src.semantic_layer.build_semantic_layer

    Optional environment variable:
        MEDFABRIC_SEMANTIC_LAYER_CONFIG

    Example:
        MEDFABRIC_SEMANTIC_LAYER_CONFIG=config/semantic_layer/semantic_layer.yaml \
        python -m src.semantic_layer.build_semantic_layer
    """

    config_path = os.environ.get(
        "MEDFABRIC_SEMANTIC_LAYER_CONFIG",
        DEFAULT_CONFIG_PATH,
    )

    result = build_semantic_layer(config_path=config_path)

    if result.status == AUDIT_STATUS_SUCCESS:
        print(result.message)
        return

    print(f"MedFabric Semantic Layer failed: {result.message}")
    sys.exit(1)


if __name__ == "__main__":
    main()