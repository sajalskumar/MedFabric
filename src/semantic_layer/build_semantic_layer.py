def calculate_metric(
    dataframe: pd.DataFrame,
    metric_config: Dict[str, Any],
) -> Any:
    """
    Calculate a metric from a dataframe.

    Supported calculation types:
        - count_rows
        - count_distinct
        - sum
        - mean
        - min
        - max
        - median

    Args:
        dataframe:
            Source dataframe.

        metric_config:
            Metric configuration.

    Returns:
        Any:
            Calculated metric value.
    """

    calculation_type = metric_config.get("calculation_type")
    column = metric_config.get("column")

    if calculation_type == "count_rows":
        return int(len(dataframe))

    if calculation_type == "count_distinct":
        if not column:
            raise ValueError("count_distinct metric requires 'column'.")
        return int(dataframe[column].nunique(dropna=True))

    if calculation_type == "sum":
        if not column:
            raise ValueError("sum metric requires 'column'.")
        return dataframe[column].sum()

    if calculation_type == "mean":
        if not column:
            raise ValueError("mean metric requires 'column'.")
        return dataframe[column].mean()

    if calculation_type == "min":
        if not column:
            raise ValueError("min metric requires 'column'.")
        return dataframe[column].min()

    if calculation_type == "max":
        if not column:
            raise ValueError("max metric requires 'column'.")
        return dataframe[column].max()

    if calculation_type == "median":
        if not column:
            raise ValueError("median metric requires 'column'.")
        return dataframe[column].median()

    raise ValueError(f"Unsupported metric calculation_type: {calculation_type}")


def build_semantic_metrics(
    runtime: SemanticLayerRuntime,
    datasets: Dict[str, pd.DataFrame],
) -> Dict[str, pd.DataFrame]:
    """
    Build semantic metrics.

    Configuration example:
        semantic_metrics:
          enabled: true
          metric_groups:
            population_metrics:
              source_dataset: member_features
              grain: platform
              metrics:
                total_members:
                  calculation_type: count_rows
                  description: Total members in the feature store.
                unique_members:
                  calculation_type: count_distinct
                  column: member_id
                  description: Unique members.

    Args:
        runtime:
            Runtime context.

        datasets:
            Loaded Feature Store datasets.

    Returns:
        Dict[str, pd.DataFrame]:
            Metric group dataframes.
    """

    logger = runtime.logger
    config = runtime.config.get("semantic_metrics", {})
    enabled = bool(config.get("enabled", True))

    metric_outputs: Dict[str, pd.DataFrame] = {}

    if not enabled:
        logger.info("SKIP: Semantic metrics disabled by configuration")
        add_audit_record(
            runtime=runtime,
            step_name="build_semantic_metrics",
            status=AUDIT_STATUS_SKIPPED,
            message="Semantic metrics disabled by configuration.",
        )
        return metric_outputs

    metric_groups_config = config.get("metric_groups", {})

    logger.info("START: Build semantic metrics")

    for metric_group_name, metric_group_config in metric_groups_config.items():
        source_dataset_name = metric_group_config.get("source_dataset")
        grain = metric_group_config.get("grain", "platform")
        owner = metric_group_config.get("owner", "MedFabric")
        metrics_config = metric_group_config.get("metrics", {})

        if source_dataset_name not in datasets:
            message = (
                f"Source dataset '{source_dataset_name}' not loaded for "
                f"metric group '{metric_group_name}'."
            )
            logger.warning(message)
            add_audit_record(
                runtime=runtime,
                step_name=f"build_semantic_metric_group:{metric_group_name}",
                status=AUDIT_STATUS_SKIPPED,
                message=message,
            )
            continue

        source_df = datasets[source_dataset_name]
        metric_rows: List[Dict[str, Any]] = []

        for metric_name, metric_config in metrics_config.items():
            try:
                metric_value = calculate_metric(source_df, metric_config)
                description = metric_config.get("description", "")
                calculation_type = metric_config.get("calculation_type", "")
                column = metric_config.get("column", "")

                metric_rows.append(
                    {
                        "metric_group": metric_group_name,
                        "metric_name": metric_name,
                        "metric_value": metric_value,
                        "metric_grain": grain,
                        "source_dataset": source_dataset_name,
                        "calculation_type": calculation_type,
                        "source_column": column,
                        "description": description,
                        "semantic_layer_run_id": runtime.run_id,
                        "semantic_layer_built_at_utc": utc_now().isoformat(),
                    }
                )

                add_metric_record(
                    runtime=runtime,
                    metric_name=metric_name,
                    metric_group=metric_group_name,
                    description=description,
                    source_dataset=source_dataset_name,
                    calculation=f"{calculation_type}({column})",
                    grain=grain,
                    owner=owner,
                )

                add_validation_record(
                    runtime=runtime,
                    dataset_name=metric_group_name,
                    rule_name=f"metric_calculation:{metric_name}",
                    status=AUDIT_STATUS_SUCCESS,
                    message="Metric calculated successfully.",
                    failed_count=0,
                )

            except Exception as exc:
                logger.error(
                    "Failed metric calculation | Metric Group: %s | Metric: %s | Error: %s",
                    metric_group_name,
                    metric_name,
                    exc,
                )

                add_validation_record(
                    runtime=runtime,
                    dataset_name=metric_group_name,
                    rule_name=f"metric_calculation:{metric_name}",
                    status=AUDIT_STATUS_FAILED,
                    message=str(exc),
                    failed_count=1,
                )

                if bool(metric_config.get("required", True)):
                    raise

        metric_df = pd.DataFrame(metric_rows)
        metric_outputs[metric_group_name] = metric_df

        logger.info(
            "Built semantic metric group: %s | Metrics: %s",
            metric_group_name,
            len(metric_df),
        )

        add_dataset_record(
            runtime=runtime,
            dataset_name=metric_group_name,
            dataset_type="semantic_metric_group",
            status=AUDIT_STATUS_SUCCESS,
            path=metric_group_config.get("output_path"),
            row_count=len(metric_df),
            column_count=len(metric_df.columns),
            message=f"Semantic metric group built from source dataset '{source_dataset_name}'.",
        )

    logger.info("COMPLETE: Build semantic metrics | Groups: %s", len(metric_outputs))

    add_audit_record(
        runtime=runtime,
        step_name="build_semantic_metrics",
        status=AUDIT_STATUS_SUCCESS,
        message=f"Semantic metric groups built: {len(metric_outputs)}",
    )

    return metric_outputs


