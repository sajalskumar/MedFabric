def utc_now() -> datetime:
    """
    Return the current UTC timestamp.

    Returns:
        datetime:
            Timezone-aware UTC timestamp.
    """

    return datetime.now(timezone.utc)


def generate_run_id() -> str:
    """
    Generate a timestamp-based run identifier.

    Returns:
        str:
            Run ID in YYYYMMDD_HHMMSS format.
    """

    return utc_now().strftime("%Y%m%d_%H%M%S")


def normalize_path(project_root: Path, raw_path: str | Path) -> Path:
    """
    Resolve a configured path relative to the project root.

    Args:
        project_root:
            Root directory of the MedFabric repository.

        raw_path:
            Path from configuration. May be absolute or relative.

    Returns:
        Path:
            Resolved absolute path.
    """

    path = Path(raw_path)

    if path.is_absolute():
        return path

    return project_root / path


def ensure_directory(path: Path) -> None:
    """
    Create a directory if it does not exist.

    Args:
        path:
            Directory path to create.
    """

    path.mkdir(parents=True, exist_ok=True)


def safe_string(value: Any) -> str:
    """
    Convert a value into a safe string.

    Args:
        value:
            Any Python value.

    Returns:
        str:
            String representation.
    """

    if value is None:
        return ""

    return str(value)


def flatten_dict(
    data: Dict[str, Any],
    parent_key: str = "",
    separator: str = ".",
) -> Dict[str, Any]:
    """
    Flatten a nested dictionary for easier metadata reporting.

    Args:
        data:
            Nested dictionary.

        parent_key:
            Prefix used during recursion.

        separator:
            Separator between nested keys.

    Returns:
        Dict[str, Any]:
            Flattened dictionary.
    """

    flattened: Dict[str, Any] = {}

    for key, value in data.items():
        new_key = f"{parent_key}{separator}{key}" if parent_key else str(key)

        if isinstance(value, dict):
            flattened.update(flatten_dict(value, new_key, separator))
        else:
            flattened[new_key] = value

    return flattened


