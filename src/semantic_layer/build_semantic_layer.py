def build_semantic_business_views(
    runtime: SemanticLayerRuntime,
    datasets: Dict[str, pd.DataFrame],
    semantic_dimensions: Dict[str, pd.DataFrame],
    semantic_metrics: Dict[str, pd.DataFrame],
) -> Dict[str, pd.DataFrame]:
    """
    Build semantic business views.

    Business views are configuration-defined analytics-ready datasets. A view can
    currently be produced by:
        - Copying a source dataset
        - Selecting/renaming columns
        - Adding metadata columns

    This function intentionally keeps joins conservative. If a project requires
    complex joins, they should be declared clearly in YAML and implemented with
    explicit validation.

    Args:
        runtime:
            Runtime context.

        datasets:
            Loaded Feature Store datasets.

        semantic_dimensions:
            Built semantic dimensions.

        semantic_metrics:
            Built semantic metric groups.

    Returns:
        Dict[str, pd.DataFrame]:
            Business view dataframes.
    """

    logger = runtime.logger
    config = runtime.config.get("semantic_business_views", {})
    enabled = bool(config.get("enabled", True))

    business_views: Dict[str, pd.DataFrame] = {}

    if not enabled:
        logger.info("SKIP: Semantic business views disabled by configuration")
        add_audit_record(
            runtime=runtime,
            step_name="build_semantic_business_views",
            status=AUDIT_STATUS_SKIPPED,
            message="Semantic business views disabled by configuration.",
        )
        return business_views

    views_config = config.get("views", {})

    available_sources: Dict[str, pd.DataFrame] = {}
    available_sources.update(datasets)
    available_sources.update(semantic_dimensions)
    available_sources.update(semantic_metrics)

    logger.info("START: Build semantic business views")

    for view_name, view_config in views_config.items():
        source_dataset_name = view_config.get("source_dataset")
        column_mappings = view_config.get("column_mappings", {})
        keep_unmapped = bool(view_config.get("keep_unmapped_columns", True))

        if source_dataset_name not in available_sources:
            message = (
                f"Source dataset '{source_dataset_name}' not available for "
                f"business view '{view_name}'."
            )
            logger.warning(message)
            add_audit_record(
                runtime=runtime,
                step_name=f"build_semantic_business_view:{view_name}",
                status=AUDIT_STATUS_SKIPPED,
                message=message,
            )
            continue

        source_df = available_sources[source_dataset_name]

        if column_mappings:
            view_df = select_and_rename_columns(
                dataframe=source_df,
                column_mappings=column_mappings,
                keep_unmapped=keep_unmapped,
            )
        else:
            view_df = source_df.copy()

        filters_config = view_config.get("filters", [])
        view_df = apply_simple_filters(view_df, filters_config)

        view_df = add_standard_semantic_columns(
            dataframe=view_df,
            runtime=runtime,
            semantic_asset_name=view_name,
        )

        business_views[view_name] = view_df

        logger.info(
            "Built semantic business view: %s | Source: %s | Rows: %s | Columns: %s",
            view_name,
            source_dataset_name,
            len(view_df),
            len(view_df.columns),
        )

        add_dataset_record(
            runtime=runtime,
            dataset_name=view_name,
            dataset_type="semantic_business_view",
            status=AUDIT_STATUS_SUCCESS,
            path=view_config.get("output_path"),
            row_count=len(view_df),
            column_count=len(view_df.columns),
            message=f"Semantic business view built from source dataset '{source_dataset_name}'.",
        )

    logger.info(
        "COMPLETE: Build semantic business views | Count: %s",
        len(business_views),
    )

    add_audit_record(
        runtime=runtime,
        step_name="build_semantic_business_views",
        status=AUDIT_STATUS_SUCCESS,
        message=f"Semantic business views built: {len(business_views)}",
    )

    return business_views


def apply_simple_filters(
    dataframe: pd.DataFrame,
    filters_config: List[Dict[str, Any]],
) -> pd.DataFrame:
    """
    Apply simple configuration-defined filters to a dataframe.

    Supported operators:
        - equals
        - not_equals
        - greater_than
        - greater_than_or_equal
        - less_than
        - less_than_or_equal
        - in
        - not_in
        - is_not_null
        - is_null

    Args:
        dataframe:
            Source dataframe.

        filters_config:
            List of filter definitions.

    Returns:
        pd.DataFrame:
            Filtered dataframe.
    """

    result = dataframe.copy()

    for filter_config in filters_config:
        column = filter_config.get("column")
        operator = filter_config.get("operator")
        value = filter_config.get("value")

        if not column or column not in result.columns:
            continue

        if operator == "equals":
            result = result[result[column] == value]
        elif operator == "not_equals":
            result = result[result[column] != value]
        elif operator == "greater_than":
            result = result[result[column] > value]
        elif operator == "greater_than_or_equal":
            result = result[result[column] >= value]
        elif operator == "less_than":
            result = result[result[column] < value]
        elif operator == "less_than_or_equal":
            result = result[result[column] <= value]
        elif operator == "in":
            result = result[result[column].isin(value)]
        elif operator == "not_in":
            result = result[~result[column].isin(value)]
        elif operator == "is_not_null":
            result = result[result[column].notna()]
        elif operator == "is_null":
            result = result[result[column].isna()]

    return result


