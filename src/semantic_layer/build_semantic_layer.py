def load_feature_store_datasets(runtime: SemanticLayerRuntime) -> Dict[str, pd.DataFrame]:
    """
    Load configured Feature Store datasets.

    The configuration should define input datasets under:
        paths:
          inputs:
            dataset_name:
              path: data/feature_store/example.parquet
              format: parquet
              required: true

    Args:
        runtime:
            Runtime context.

    Returns:
        Dict[str, pd.DataFrame]:
            Mapping of dataset name to dataframe.
    """

    logger = runtime.logger
    inputs_config = get_inputs_config(runtime)

    datasets: Dict[str, pd.DataFrame] = {}

    logger.info("START: Load Feature Store datasets")

    for dataset_name, dataset_config in inputs_config.items():
        if not isinstance(dataset_config, dict):
            add_validation_record(
                runtime=runtime,
                dataset_name=dataset_name,
                rule_name="input_config_shape",
                status=AUDIT_STATUS_FAILED,
                message="Input dataset configuration must be a dictionary.",
            )
            raise ValueError(
                f"Input configuration for '{dataset_name}' must be a dictionary."
            )

        required = bool(dataset_config.get("required", True))
        raw_path = dataset_config.get("path")
        file_format = dataset_config.get("format")

        if not raw_path:
            message = f"No path configured for input dataset: {dataset_name}"

            if required:
                add_validation_record(
                    runtime=runtime,
                    dataset_name=dataset_name,
                    rule_name="input_path_required",
                    status=AUDIT_STATUS_FAILED,
                    message=message,
                )
                raise ValueError(message)

            logger.warning("SKIP optional dataset with no path: %s", dataset_name)
            continue

        dataset_path = normalize_path(runtime.project_root, raw_path)

        try:
            dataframe = read_dataset(dataset_path, file_format)
            datasets[dataset_name] = dataframe

            logger.info(
                "Loaded dataset: %s | Rows: %s | Columns: %s | Path: %s",
                dataset_name,
                len(dataframe),
                len(dataframe.columns),
                dataset_path,
            )

            add_dataset_record(
                runtime=runtime,
                dataset_name=dataset_name,
                dataset_type="feature_store_input",
                status=AUDIT_STATUS_SUCCESS,
                path=str(dataset_path),
                row_count=len(dataframe),
                column_count=len(dataframe.columns),
                message="Feature Store dataset loaded successfully.",
            )

            add_audit_record(
                runtime=runtime,
                step_name=f"load_input_dataset:{dataset_name}",
                status=AUDIT_STATUS_SUCCESS,
                message="Dataset loaded successfully.",
                row_count=len(dataframe),
                output_path=str(dataset_path),
            )

        except Exception as exc:
            message = f"Failed to load input dataset '{dataset_name}': {exc}"

            if required:
                logger.error(message)
                add_audit_record(
                    runtime=runtime,
                    step_name=f"load_input_dataset:{dataset_name}",
                    status=AUDIT_STATUS_FAILED,
                    message=message,
                    output_path=str(dataset_path),
                )
                raise

            logger.warning("Optional dataset skipped: %s | Reason: %s", dataset_name, exc)

            add_audit_record(
                runtime=runtime,
                step_name=f"load_input_dataset:{dataset_name}",
                status=AUDIT_STATUS_SKIPPED,
                message=message,
                output_path=str(dataset_path),
            )

    logger.info("COMPLETE: Load Feature Store datasets | Count: %s", len(datasets))

    return datasets


