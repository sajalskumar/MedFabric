def build_execution_summary(
    runtime: SemanticLayerRuntime,
    semantic_dimensions: Dict[str, pd.DataFrame],
    semantic_metrics: Dict[str, pd.DataFrame],
    semantic_business_views: Dict[str, pd.DataFrame],
) -> pd.DataFrame:
    """
    Build execution summary dataframe.

    Args:
        runtime:
            Runtime context.

        semantic_dimensions:
            Semantic dimensions.

        semantic_metrics:
            Semantic metrics.

        semantic_business_views:
            Semantic business views.

    Returns:
        pd.DataFrame:
            One-row execution summary.
    """

    end_time = utc_now()
    duration_seconds = (end_time - runtime.start_time_utc).total_seconds()

    failed_validations = [
        record for record in runtime.validation_records
        if record.get("status") == AUDIT_STATUS_FAILED
    ]

    summary = {
        "run_id": runtime.run_id,
        "layer_name": runtime.layer_name,
        "config_path": str(runtime.config_path),
        "start_time_utc": runtime.start_time_utc.isoformat(),
        "end_time_utc": end_time.isoformat(),
        "duration_seconds": duration_seconds,
        "semantic_dimension_count": len(semantic_dimensions),
        "semantic_metric_group_count": len(semantic_metrics),
        "semantic_business_view_count": len(semantic_business_views),
        "audit_record_count": len(runtime.audit_records),
        "validation_record_count": len(runtime.validation_records),
        "failed_validation_count": len(failed_validations),
        "dataset_record_count": len(runtime.dataset_records),
        "metric_definition_count": len(runtime.metric_records),
        "status": (
            AUDIT_STATUS_SUCCESS
            if len(failed_validations) == 0
            else AUDIT_STATUS_WARNING
        ),
    }

    return pd.DataFrame([summary])


