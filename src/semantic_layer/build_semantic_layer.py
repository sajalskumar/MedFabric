def load_yaml_config(config_path: Path) -> Dict[str, Any]:
    """
    Load YAML configuration.

    Args:
        config_path:
            Path to YAML configuration file.

    Returns:
        Dict[str, Any]:
            Parsed YAML configuration.

    Raises:
        FileNotFoundError:
            If the configuration file does not exist.

        ValueError:
            If the configuration is empty or not a dictionary.
    """

    if not config_path.exists():
        raise FileNotFoundError(f"Configuration file not found: {config_path}")

    with config_path.open("r", encoding="utf-8") as config_file:
        config = yaml.safe_load(config_file)

    if config is None:
        raise ValueError(f"Configuration file is empty: {config_path}")

    if not isinstance(config, dict):
        raise ValueError(
            f"Configuration file must contain a YAML mapping/object: {config_path}"
        )

    return config


def validate_required_config_sections(config: Dict[str, Any]) -> List[str]:
    """
    Validate that the minimum required configuration sections exist.

    Args:
        config:
            Semantic layer configuration.

    Returns:
        List[str]:
            Validation error messages.
    """

    errors: List[str] = []

    required_sections = [
        "semantic_layer",
        "paths",
    ]

    for section in required_sections:
        if section not in config:
            errors.append(f"Missing required configuration section: {section}")

    paths = config.get("paths", {})

    if not isinstance(paths, dict):
        errors.append("Configuration section 'paths' must be a dictionary.")
    else:
        if "inputs" not in paths:
            errors.append("Missing required configuration section: paths.inputs")

        if "outputs" not in paths:
            errors.append("Missing required configuration section: paths.outputs")

    return errors


def validate_config(config: Dict[str, Any]) -> None:
    """
    Validate semantic layer configuration.

    Args:
        config:
            Loaded YAML configuration.

    Raises:
        ValueError:
            If required configuration is missing or invalid.
    """

    errors = validate_required_config_sections(config)

    output_format = (
        config.get("semantic_layer", {})
        .get("output_format", DEFAULT_OUTPUT_FORMAT)
    )

    if output_format not in SUPPORTED_FILE_FORMATS:
        errors.append(
            f"Unsupported output_format '{output_format}'. "
            f"Supported formats: {sorted(SUPPORTED_FILE_FORMATS)}"
        )

    if errors:
        error_message = "\n".join(f"- {error}" for error in errors)
        raise ValueError(f"Semantic layer configuration validation failed:\n{error_message}")


