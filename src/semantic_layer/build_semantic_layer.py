def validate_required_columns(
    runtime: SemanticLayerRuntime,
    dataframe: pd.DataFrame,
    dataset_name: str,
    required_columns: Iterable[str],
) -> bool:
    """
    Validate that required columns exist in a dataframe.

    Args:
        runtime:
            Runtime context.

        dataframe:
            Dataframe to validate.

        dataset_name:
            Logical dataset name.

        required_columns:
            Required column names.

    Returns:
        bool:
            True if validation passes, otherwise False.
    """

    required_columns_list = list(required_columns)
    missing_columns = [
        column for column in required_columns_list
        if column not in dataframe.columns
    ]

    if missing_columns:
        add_validation_record(
            runtime=runtime,
            dataset_name=dataset_name,
            rule_name="required_columns",
            status=AUDIT_STATUS_FAILED,
            message=f"Missing required columns: {missing_columns}",
            failed_count=len(missing_columns),
        )
        return False

    add_validation_record(
        runtime=runtime,
        dataset_name=dataset_name,
        rule_name="required_columns",
        status=AUDIT_STATUS_SUCCESS,
        message="All required columns are present.",
        failed_count=0,
    )

    return True


def validate_not_empty(
    runtime: SemanticLayerRuntime,
    dataframe: pd.DataFrame,
    dataset_name: str,
) -> bool:
    """
    Validate that a dataframe is not empty.

    Args:
        runtime:
            Runtime context.

        dataframe:
            Dataframe to validate.

        dataset_name:
            Logical dataset name.

    Returns:
        bool:
            True if not empty, otherwise False.
    """

    if dataframe.empty:
        add_validation_record(
            runtime=runtime,
            dataset_name=dataset_name,
            rule_name="not_empty",
            status=AUDIT_STATUS_FAILED,
            message="Dataset is empty.",
            failed_count=1,
        )
        return False

    add_validation_record(
        runtime=runtime,
        dataset_name=dataset_name,
        rule_name="not_empty",
        status=AUDIT_STATUS_SUCCESS,
        message="Dataset is not empty.",
        failed_count=0,
    )

    return True


def validate_unique_key(
    runtime: SemanticLayerRuntime,
    dataframe: pd.DataFrame,
    dataset_name: str,
    key_columns: Iterable[str],
) -> bool:
    """
    Validate uniqueness of key columns.

    Args:
        runtime:
            Runtime context.

        dataframe:
            Dataframe to validate.

        dataset_name:
            Logical dataset name.

        key_columns:
            Columns that should uniquely identify rows.

    Returns:
        bool:
            True if unique, otherwise False.
    """

    key_columns_list = list(key_columns)

    if not validate_required_columns(runtime, dataframe, dataset_name, key_columns_list):
        return False

    duplicate_count = int(dataframe.duplicated(subset=key_columns_list).sum())

    if duplicate_count > 0:
        add_validation_record(
            runtime=runtime,
            dataset_name=dataset_name,
            rule_name="unique_key",
            status=AUDIT_STATUS_FAILED,
            message=f"Duplicate records found for key columns: {key_columns_list}",
            failed_count=duplicate_count,
        )
        return False

    add_validation_record(
        runtime=runtime,
        dataset_name=dataset_name,
        rule_name="unique_key",
        status=AUDIT_STATUS_SUCCESS,
        message=f"Key columns are unique: {key_columns_list}",
        failed_count=0,
    )

    return True


