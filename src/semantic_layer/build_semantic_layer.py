def build_semantic_dimensions(
    runtime: SemanticLayerRuntime,
    datasets: Dict[str, pd.DataFrame],
) -> Dict[str, pd.DataFrame]:
    """
    Build semantic dimensions.

    Configuration example:
        semantic_dimensions:
          enabled: true
          dimensions:
            member_semantic_dimension:
              source_dataset: member_features
              key_columns:
                - member_id
              column_mappings:
                member_id: member_id
                age: member_age
                gender: member_gender
              output_path: data/semantic_layer/dimensions/member_semantic_dimension.parquet

    Args:
        runtime:
            Runtime context.

        datasets:
            Loaded Feature Store datasets.

    Returns:
        Dict[str, pd.DataFrame]:
            Built semantic dimension dataframes.
    """

    logger = runtime.logger
    config = runtime.config.get("semantic_dimensions", {})
    enabled = bool(config.get("enabled", True))

    semantic_dimensions: Dict[str, pd.DataFrame] = {}

    if not enabled:
        logger.info("SKIP: Semantic dimensions disabled by configuration")
        add_audit_record(
            runtime=runtime,
            step_name="build_semantic_dimensions",
            status=AUDIT_STATUS_SKIPPED,
            message="Semantic dimensions disabled by configuration.",
        )
        return semantic_dimensions

    dimensions_config = config.get("dimensions", {})

    logger.info("START: Build semantic dimensions")

    for dimension_name, dimension_config in dimensions_config.items():
        source_dataset_name = dimension_config.get("source_dataset")
        key_columns = dimension_config.get("key_columns", [])
        column_mappings = dimension_config.get("column_mappings", {})
        keep_unmapped = bool(dimension_config.get("keep_unmapped_columns", False))

        if source_dataset_name not in datasets:
            message = (
                f"Source dataset '{source_dataset_name}' not loaded for "
                f"semantic dimension '{dimension_name}'."
            )
            logger.warning(message)
            add_audit_record(
                runtime=runtime,
                step_name=f"build_semantic_dimension:{dimension_name}",
                status=AUDIT_STATUS_SKIPPED,
                message=message,
            )
            continue

        source_df = datasets[source_dataset_name]

        validate_not_empty(runtime, source_df, source_dataset_name)

        if key_columns:
            validate_unique_key(runtime, source_df, source_dataset_name, key_columns)

        if column_mappings:
            semantic_df = select_and_rename_columns(
                dataframe=source_df,
                column_mappings=column_mappings,
                keep_unmapped=keep_unmapped,
            )
        else:
            semantic_df = source_df.copy()

        semantic_df = add_standard_semantic_columns(
            dataframe=semantic_df,
            runtime=runtime,
            semantic_asset_name=dimension_name,
        )

        semantic_dimensions[dimension_name] = semantic_df

        logger.info(
            "Built semantic dimension: %s | Source: %s | Rows: %s | Columns: %s",
            dimension_name,
            source_dataset_name,
            len(semantic_df),
            len(semantic_df.columns),
        )

        add_dataset_record(
            runtime=runtime,
            dataset_name=dimension_name,
            dataset_type="semantic_dimension",
            status=AUDIT_STATUS_SUCCESS,
            path=dimension_config.get("output_path"),
            row_count=len(semantic_df),
            column_count=len(semantic_df.columns),
            message=f"Semantic dimension built from source dataset '{source_dataset_name}'.",
        )

    logger.info(
        "COMPLETE: Build semantic dimensions | Count: %s",
        len(semantic_dimensions),
    )

    add_audit_record(
        runtime=runtime,
        step_name="build_semantic_dimensions",
        status=AUDIT_STATUS_SUCCESS,
        message=f"Semantic dimensions built: {len(semantic_dimensions)}",
    )

    return semantic_dimensions


