    ensure_directory(default_output_dir)

    for asset_name, dataframe in assets.items():
        asset_config = child_config.get(asset_name, {})
        output_path_raw = asset_config.get(
            "output_path",
            str(default_output_dir / asset_name),
        )

        output_path = normalize_path(runtime.project_root, output_path_raw)
        output_path = output_path_with_format(output_path, output_format)

        write_dataset(dataframe, output_path, output_format)

        logger.info(
            "Wrote %s asset: %s | Rows: %s | Path: %s",
            asset_group_name,
            asset_name,
            len(dataframe),
            output_path,
        )

        add_audit_record(
            runtime=runtime,
            step_name=f"write_output:{asset_name}",
            status=AUDIT_STATUS_SUCCESS,
            message=f"Output written for {asset_group_name}.",
            row_count=len(dataframe),
            output_path=str(output_path),
        )


