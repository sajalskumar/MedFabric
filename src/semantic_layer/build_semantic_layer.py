def configure_logging(project_root: Path, config: Dict[str, Any], run_id: str) -> logging.Logger:
    """
    Configure centralized logging for the Semantic Layer.

    Logging paths are read from configuration when provided. If the configuration
    does not provide logging paths, this function uses the established MedFabric
    logging pattern under logs/modules.

    Args:
        project_root:
            Repository root.

        config:
            Semantic layer configuration dictionary.

        run_id:
            Current run identifier.

    Returns:
        logging.Logger:
            Configured logger.
    """

    logging_config = config.get("logging", {})

    log_level_name = logging_config.get("level", "INFO")
    log_level = getattr(logging, str(log_level_name).upper(), logging.INFO)

    module_log_dir_raw = (
        logging_config.get("module_log_dir")
        or logging_config.get("semantic_layer_log_dir")
        or "logs/modules"
    )

    module_log_dir = normalize_path(project_root, module_log_dir_raw)
    ensure_directory(module_log_dir)

    log_file_name = logging_config.get(
        "log_file_name",
        f"semantic_layer_{run_id}.log",
    )

    log_file_path = module_log_dir / log_file_name

    logger = logging.getLogger("medfabric.semantic_layer")
    logger.setLevel(log_level)
    logger.handlers.clear()
    logger.propagate = False

    formatter = logging.Formatter(
        fmt="[%(asctime)s] [RUN_ID=%(run_id)s] [%(levelname)s] %(message)s",
        datefmt="%Y-%m-%d %H:%M:%S",
    )

    class RunIdFilter(logging.Filter):
        def filter(self, record: logging.LogRecord) -> bool:
            record.run_id = run_id
            return True

    stream_handler = logging.StreamHandler(sys.stdout)
    stream_handler.setLevel(log_level)
    stream_handler.setFormatter(formatter)
    stream_handler.addFilter(RunIdFilter())

    file_handler = logging.FileHandler(log_file_path, encoding="utf-8")
    file_handler.setLevel(log_level)
    file_handler.setFormatter(formatter)
    file_handler.addFilter(RunIdFilter())

    logger.addHandler(stream_handler)
    logger.addHandler(file_handler)

    logger.info("=" * 80)
    logger.info("MedFabric Semantic Layer execution started")
    logger.info("=" * 80)
    logger.info("Run ID: %s", run_id)
    logger.info("Log file: %s", log_file_path)

    return logger


