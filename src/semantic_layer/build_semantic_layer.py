def build_semantic_layer(config_path: str = DEFAULT_CONFIG_PATH) -> DatasetResult:
    """
    Build the complete MedFabric Semantic Layer.

    Args:
        config_path:
            Path to semantic layer YAML configuration.

    Returns:
        DatasetResult:
            Final execution result.
    """

    runtime: Optional[SemanticLayerRuntime] = None

    try:
        runtime = initialize_runtime(config_path)
        logger = runtime.logger

        logger.info("Semantic Layer configuration path: %s", runtime.config_path)

        datasets = load_feature_store_datasets(runtime)

        semantic_dimensions = build_semantic_dimensions(runtime, datasets)

        semantic_metrics = build_semantic_metrics(runtime, datasets)

        semantic_business_views = build_semantic_business_views(
            runtime=runtime,
            datasets=datasets,
            semantic_dimensions=semantic_dimensions,
            semantic_metrics=semantic_metrics,
        )

        all_semantic_assets: Dict[str, pd.DataFrame] = {}
        all_semantic_assets.update(semantic_dimensions)
        all_semantic_assets.update(semantic_metrics)
        all_semantic_assets.update(semantic_business_views)

        metadata_catalogs = {
            "semantic_metadata_catalog": build_semantic_metadata_catalog(
                runtime=runtime,
                all_assets=all_semantic_assets,
            ),
            "semantic_entity_mapping_catalog": build_entity_mapping_catalog(runtime),
            "semantic_configuration_catalog": build_configuration_catalog(runtime),
        }

        write_configured_outputs(
            runtime=runtime,
            semantic_dimensions=semantic_dimensions,
            semantic_metrics=semantic_metrics,
            semantic_business_views=semantic_business_views,
            metadata_catalogs=metadata_catalogs,
        )

        summary_df = build_execution_summary(
            runtime=runtime,
            semantic_dimensions=semantic_dimensions,
            semantic_metrics=semantic_metrics,
            semantic_business_views=semantic_business_views,
        )

        add_audit_record(
            runtime=runtime,
            step_name="build_semantic_layer",
            status=AUDIT_STATUS_SUCCESS,
            message="Semantic Layer completed successfully.",
        )

        write_audit_outputs(runtime)
        write_execution_summary(runtime, summary_df)

        logger.info("=" * 80)
        logger.info("MedFabric Semantic Layer completed successfully")
        logger.info(
            "Dimensions: %s | Metric Groups: %s | Business Views: %s",
            len(semantic_dimensions),
            len(semantic_metrics),
            len(semantic_business_views),
        )
        logger.info("=" * 80)

        return DatasetResult(
            name="semantic_layer",
            status=AUDIT_STATUS_SUCCESS,
            row_count=len(summary_df),
            column_count=len(summary_df.columns),
            message="MedFabric Semantic Layer completed successfully.",
        )

    except Exception as exc:
        if runtime is not None:
            runtime.logger.error("=" * 80)
            runtime.logger.error("MedFabric Semantic Layer failed")
            runtime.logger.error("Error: %s", exc)
            runtime.logger.error("Traceback:\n%s", traceback.format_exc())
            runtime.logger.error("=" * 80)

            add_audit_record(
                runtime=runtime,
                step_name="build_semantic_layer",
                status=AUDIT_STATUS_FAILED,
                message=str(exc),
            )

            try:
                write_audit_outputs(runtime)
            except Exception as audit_exc:
                runtime.logger.error("Failed to write audit outputs: %s", audit_exc)

        return DatasetResult(
            name="semantic_layer",
            status=AUDIT_STATUS_FAILED,
            message=str(exc),
        )


