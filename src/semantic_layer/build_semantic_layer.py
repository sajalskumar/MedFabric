def write_configured_outputs(
    runtime: SemanticLayerRuntime,
    semantic_dimensions: Dict[str, pd.DataFrame],
    semantic_metrics: Dict[str, pd.DataFrame],
    semantic_business_views: Dict[str, pd.DataFrame],
    metadata_catalogs: Dict[str, pd.DataFrame],
) -> None:
    """
    Write all configured semantic outputs.

    Args:
        runtime:
            Runtime context.

        semantic_dimensions:
            Built semantic dimensions.

        semantic_metrics:
            Built semantic metric groups.

        semantic_business_views:
            Built semantic business views.

        metadata_catalogs:
            Built metadata catalog dataframes.
    """

    logger = runtime.logger
    output_format = get_output_format(runtime)

    logger.info("START: Write semantic layer outputs")

