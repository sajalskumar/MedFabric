def select_and_rename_columns(
    dataframe: pd.DataFrame,
    column_mappings: Dict[str, str],
    keep_unmapped: bool = False,
) -> pd.DataFrame:
    """
    Select and rename columns using a semantic mapping.

    Args:
        dataframe:
            Source dataframe.

        column_mappings:
            Mapping of source column name to semantic column name.

        keep_unmapped:
            Whether to retain unmapped source columns.

    Returns:
        pd.DataFrame:
            Transformed dataframe.
    """

    available_mappings = {
        source_column: semantic_column
        for source_column, semantic_column in column_mappings.items()
        if source_column in dataframe.columns
    }

    if keep_unmapped:
        result = dataframe.copy()
        result = result.rename(columns=available_mappings)
        return result

    selected_columns = list(available_mappings.keys())
    result = dataframe[selected_columns].copy()
    result = result.rename(columns=available_mappings)

    return result


