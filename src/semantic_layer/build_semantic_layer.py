def read_dataset(path: Path, file_format: Optional[str] = None) -> pd.DataFrame:
    """
    Read a dataset from disk.

    Args:
        path:
            Dataset path.

        file_format:
            Optional explicit file format. If omitted, inferred from suffix.

    Returns:
        pd.DataFrame:
            Loaded dataframe.

    Raises:
        FileNotFoundError:
            If the path does not exist.

        ValueError:
            If the file format is unsupported.
    """

    if not path.exists():
        raise FileNotFoundError(f"Input dataset not found: {path}")

    resolved_format = file_format or path.suffix.replace(".", "").lower()

    if resolved_format == "parquet":
        return pd.read_parquet(path)

    if resolved_format == "csv":
        return pd.read_csv(path)

    if resolved_format == "json":
        return pd.read_json(path)

    raise ValueError(
        f"Unsupported input file format '{resolved_format}' for path: {path}"
    )


def write_dataset(
    dataframe: pd.DataFrame,
    path: Path,
    file_format: str = DEFAULT_OUTPUT_FORMAT,
) -> None:
    """
    Write a dataframe to disk.

    Args:
        dataframe:
            Dataframe to write.

        path:
            Output path.

        file_format:
            Output format.
    """

    ensure_directory(path.parent)

    if file_format == "parquet":
        dataframe.to_parquet(path, index=False)
        return

    if file_format == "csv":
        dataframe.to_csv(path, index=False)
        return

    if file_format == "json":
        dataframe.to_json(path, orient="records", indent=2)
        return

    raise ValueError(f"Unsupported output file format: {file_format}")


def output_path_with_format(path: Path, output_format: str) -> Path:
    """
    Ensure that an output path has the configured output format suffix.

    Args:
        path:
            Base output path.

        output_format:
            Output format.

    Returns:
        Path:
            Path with correct suffix.
    """

    suffix = f".{output_format}"

    if path.suffix:
        return path.with_suffix(suffix)

    return Path(str(path) + suffix)


