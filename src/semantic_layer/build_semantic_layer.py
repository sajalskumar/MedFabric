def initialize_runtime(config_path_raw: str = DEFAULT_CONFIG_PATH) -> SemanticLayerRuntime:
    """
    Initialize runtime context.

    Args:
        config_path_raw:
            Path to semantic layer YAML configuration.

    Returns:
        SemanticLayerRuntime:
            Runtime context for the run.
    """

    project_root = Path.cwd()
    config_path = normalize_path(project_root, config_path_raw)
    run_id = generate_run_id()

    config = load_yaml_config(config_path)
    validate_config(config)

    layer_name = (
        config.get("semantic_layer", {}).get("layer_name")
        or DEFAULT_LAYER_NAME
    )

    logger = configure_logging(project_root, config, run_id)

    runtime = SemanticLayerRuntime(
        run_id=run_id,
        layer_name=layer_name,
        project_root=project_root,
        config_path=config_path,
        start_time_utc=utc_now(),
        logger=logger,
        config=config,
    )

    add_audit_record(
        runtime=runtime,
        step_name="initialize_runtime",
        status=AUDIT_STATUS_SUCCESS,
        message="Runtime initialized successfully.",
    )

    return runtime


